    <p class="article">If this sounds like what you want to do, come join us. We will greet you with a hearty hand shake and offer you a place to sit among us.</p>
    <p class="article">Here at SCARS, we have members of all skill levels and experiences. We have newly licensed Amateur Radio Operators (Hams) and we have Hams that achieved their licenses more then 50 years ago.
